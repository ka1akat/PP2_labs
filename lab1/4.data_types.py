#Data types

#string
a="Lets get excelent points"
print(type(a))

#integer
b=17
print(type(b))

#float
c=20.5
print(type(c))

#complex numbers
d=1j
print(type(d))

#list
e=["Kaka", "Tomi", "Bota"]
print(type(e))

#tuple
f=("Lemon", "Tvorog", "cherry")
print(type(f))

#range until
g=range(5)
print(type(g))

#dictionary
h={"name" : "Kakos", "age" : 17}
print(type(h))

#set
i={"", "banana", "cherry"}
print(type(i))

#frozenset
j=frozenset({"Cofee", "Milk", "Soda"})
print(type(j))

#bool
k=False
print(type(k))

#bytes
l= b"example"
print(type(l))
#print(b[0]) #101 (код 'e')

#bytearray
ba = bytearray(8)
print(type(ba))

#memoryview
z = b"hello"
mv = memoryview(z)
print(type(mv))

#NoneType
t=None
print(type(t))

